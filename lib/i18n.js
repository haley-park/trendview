// 숫자 포맷 (조회수 등) — 언어별 단위
export function makeFmt(lang) {
  if (lang === "en") {
    return (n) => {
      n = n || 0;
      if (n >= 1e9) return (n / 1e9).toFixed(1).replace(/\.0$/, "") + "B";
      if (n >= 1e6) return (n / 1e6).toFixed(1).replace(/\.0$/, "") + "M";
      if (n >= 1e3) return (n / 1e3).toFixed(1).replace(/\.0$/, "") + "K";
      return n.toLocaleString("en-US");
    };
  }
  const units = { ko: ["만", "억"], zh: ["万", "亿"], ja: ["万", "億"] }[lang] || ["만", "억"];
  return (n) => {
    n = n || 0;
    if (n >= 1e8) return (n / 1e8).toFixed(1).replace(/\.0$/, "") + units[1];
    if (n >= 1e4) return (n / 1e4).toFixed(1).replace(/\.0$/, "") + units[0];
    if (n >= 1e3) return (n / 1e3).toFixed(1).replace(/\.0$/, "") + (lang === "ko" ? "천" : "");
    return n.toLocaleString();
  };
}
